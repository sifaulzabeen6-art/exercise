class Assignment
{
	public static void main(String[]args)
	{
		int a=10;
		a+=5;
		System.out.println("the addition:"+a);
		a-=5;
		System.out.println("the subtraction:"+a);
		a*=5;
		System.out.println("the multiplication:"+a);
		a/=5;
		System.out.println("the quotient:"+a);
		a%=5;
		System.out.println("the remainder:"+a);
	}
}