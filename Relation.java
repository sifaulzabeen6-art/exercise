class Relation
{
	public static void main(String[]args)
	{
		int a=10;
		int b=20;
		boolean c=a==b;
		boolean d=a>b;
		boolean e=a<b;
		boolean f=a>=b;
		boolean g=a<=b;
		System.out.println(c);
		System.out.println(d);
		System.out.println(e);
		System.out.println(f);
		System.out.println(g);
	}
}